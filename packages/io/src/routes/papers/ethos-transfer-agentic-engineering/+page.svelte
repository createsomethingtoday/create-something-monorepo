			<h2 class="section-heading">II. Background: Heidegger's Dwelling Concept</h2>

			<div class="space-y-4 leading-relaxed body-text">
				<h3 class="subsection-heading">What Does It Mean to Dwell?</h3>

				<p>
					In "Building Dwelling Thinking" (1951), Heidegger argues that dwelling is not merely residing
					in a location but a fundamental mode of being. To dwell is to be at home, to care for a place,
					to let things be what they are.
				</p>

				<blockquote class="pl-4 italic my-4 blockquote">
					"The manner in which we humans are on the earth, is Buan, dwelling."
				</blockquote>

				<p>
					Critically, dwelling precedes building. We don't first build a house and then dwell in it.
					Rather, we build <em>because</em> we dwell—because we already have a way of being that requires
					space, tools, and shelter.
				</p>

				<h3 class="mt-6 subsection-heading">Dwelling in Methodology</h3>

				<p>
					Applied to methodology: one does not first "learn" the Subtractive Triad and then "apply" it.
					Rather, dwelling in the Triad means it becomes the way one naturally approaches creation.
					The three questions—"Have I built this before?" (DRY), "Does this earn its existence?" (Rams),
					"Does this serve the whole?" (Heidegger)—become transparent background to all creative activity.
				</p>

				<div class="grid md:grid-cols-2 gap-4 mt-6">
					<div class="p-4 comparison-warning">
						<h4 class="mb-2 comparison-heading comparison-warning-heading">Learning About (Vorhandenheit)</h4>
						<ul class="space-y-1 comparison-list">
							<li>• "I know the three levels of the Triad"</li>
							<li>• "I can recite Rams' 10 principles"</li>
							<li>• "I passed the methodology quiz"</li>
							<li>• Methodology as object of study</li>
						</ul>
					</div>

					<div class="p-4 comparison-success">
						<h4 class="mb-2 comparison-heading comparison-success-heading">Dwelling Within (Zuhandenheit)</h4>
						<ul class="space-y-1 comparison-list">
							<li>• "I noticed duplicate code before writing it"</li>
							<li>• "I removed a feature because it didn't earn its existence"</li>
							<li>• "I restructured for system coherence"</li>
							<li>• Methodology as transparent practice</li>
						</ul>
					</div>
