		<section class="space-y-6">
			<h2 class="section-heading">III. The Philosophy Lineage</h2>

			<div class="space-y-6 leading-relaxed body-text">
				<div class="master-card">
					<div class="master-header">
						<span class="master-layer-tag">Foundational</span>
						<h3 class="master-name">Martin Heidegger</h3>
						<p class="master-work">Being and Time (1927)</p>
					</div>
					<p class="master-contribution">
						Heidegger's distinction between <strong><em>Zuhandenheit</em></strong> (ready-to-hand: when a tool disappears into transparent use, like a hammer during skilled carpentry) and
						<em>Vorhandenheit</em> (present-at-hand) reveals how tools function. When a hammer
						works, we don't notice it—we notice the nail. When it breaks, the hammer becomes
						visible. This is the foundation of CREATE SOMETHING's tool philosophy:
						<strong>the best technology disappears into use</strong>.
					</p>
				</div>

				<div class="master-card">
					<div class="master-header">
						<span class="master-layer-tag">Methodological</span>
						<h3 class="master-name">Hans-Georg Gadamer</h3>
						<p class="master-work">Truth and Method (1960)</p>
					</div>
					<p class="master-contribution">
						Gadamer's <strong>hermeneutic circle</strong> (a philosophical method where understanding deepens through iterative interpretation—you understand parts through the whole, and the whole through its parts) shows how understanding works: we understand parts
						through the whole and the whole through parts. This becomes CREATE SOMETHING's
						four-property structure: .ltd (philosophy) → .io (research) → .space (practice) →
						.agency (services) → .ltd. Each property informs the others. Understanding is
						never complete—it spirals.
					</p>
				</div>

				<div class="master-card">
					<div class="master-header">
						<span class="master-layer-tag">Applied</span>
						<h3 class="master-name">Dieter Rams</h3>
						<p class="master-work">Ten Principles of Good Design</p>
					</div>
					<p class="master-contribution">
						Rams compressed decades of industrial design into ten principles, the most famous
						being "Less, but better" (<em>Weniger, aber besser</em>). His principles are
						declarative: they say what design should be, not how to achieve it. This aphoristic
						compression—principle over prescription—is the model for CREATE SOMETHING's Canon.
					</p>
				</div>
			</div>
		</section>

		<!-- Writing Lineage -->
		<section class="space-y-6">
			<h2 class="section-heading">IV. The Writing Lineage</h2>

			<div class="space-y-6 leading-relaxed body-text">
				<div class="master-card">
					<div class="master-header">
						<span class="master-layer-tag">Foundational</span>
						<h3 class="master-name">George Orwell</h3>
						<p class="master-work">Politics and the English Language (1946)</p>
					</div>
					<p class="master-contribution">
						Orwell revealed that clarity is <em>ethical</em>, not aesthetic. "Political language
						is designed to make lies sound truthful and murder respectable, and to give an
						appearance of solidity to pure wind." Bad writing isn't just unpleasant—it enables
						bad thinking. His six rules are subtractive: never use a long word where a short
						one will do; if it's possible to cut a word, cut it.
						<strong>Clarity as ethics</strong>.
					</p>
				</div>

				<div class="master-card">
					<div class="master-header">
						<span class="master-layer-tag">Methodological</span>
						<h3 class="master-name">William Zinsser</h3>
						<p class="master-work">On Writing Well (1976)</p>
					</div>
					<p class="master-contribution">
						Zinsser made Orwell's insight teachable. "Clutter is the disease of American
						writing. We are a society strangling in unnecessary words." His methodology
						is practical: simplify, prune, strip every sentence to its cleanest components.
						Where Orwell diagnosed, Zinsser prescribed. The practitioner's guide to clarity.
					</p>
				</div>

				<div class="master-card">
					<div class="master-header">
						<span class="master-layer-tag">Applied</span>
						<h3 class="master-name">Nicole Fenton & Kate Kiefer Lee</h3>
						<p class="master-work">Nicely Said (2014)</p>
					</div>
					<p class="master-contribution">
						Fenton and Lee applied clarity principles to digital writing. Their method
						emphasizes <em>recognition over confrontation</em>: help readers notice patterns,
						then transform them. They use before/after examples that show rather than tell.
						Warmth doesn't contradict austerity—it operates at a different layer.
						<strong>User-centered clarity</strong>.
					</p>
				</div>
			</div>
		</section>

		<!-- Systems Lineage -->
		<section class="space-y-6">
			<h2 class="section-heading">V. The Systems Lineage</h2>

			<div class="space-y-6 leading-relaxed body-text">
				<div class="master-card">
					<div class="master-header">
						<span class="master-layer-tag">Foundational</span>
						<h3 class="master-name">Norbert Wiener</h3>
						<p class="master-work">Cybernetics (1948)</p>
					</div>
					<p class="master-contribution">
						Wiener formalized feedback loops—the mathematics of circular causality. A thermostat
						doesn't just measure temperature; it responds to its own responses. This revealed
						a new ontology: systems aren't collections of parts but <em>patterns of behavior</em>
						that persist through feedback. The foundation of understanding interconnection.
					</p>
				</div>

				<div class="master-card">
					<div class="master-header">
						<span class="master-layer-tag">Methodological</span>
						<h3 class="master-name">Donella H. Meadows</h3>
						<p class="master-work">Thinking in Systems (2008)</p>
					</div>
					<p class="master-contribution">
						Meadows made systems thinking visible to practitioners. Her leverage points
						hierarchy—from parameters (lowest) to paradigm shifts (highest)—shows where
						intervention matters. "We can't control systems or figure them out. But we can
						dance with them." CREATE SOMETHING operates at levels 2-5: paradigm (Subtractive
						Triad), goals (hermeneutic circle), self-organization (Canon tokens), rules (Voice).
					</p>
				</div>

				<div class="master-card">
					<div class="master-header">
						<span class="master-layer-tag">Applied</span>
						<h3 class="master-name">Peter Senge</h3>
						<p class="master-work">The Fifth Discipline (1990)</p>
					</div>
					<p class="master-contribution">
						Senge brought systems thinking to organizations. Mental models, shared vision,
						team learning—these aren't soft abstractions but leverage points for organizational
						change. His work shows how systems thinking applies to teams, not just machines.
						The bridge from theory to organizational practice.
					</p>
				</div>
			</div>
		</section>

