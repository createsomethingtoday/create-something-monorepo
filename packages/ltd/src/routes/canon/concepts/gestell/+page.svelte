	<span class="concept-label">Concept</span>
	<h1 class="concept-title">Gestell</h1>
	<p class="concept-pronunciation">/ɡəˈʃtɛl/</p>
	<p class="concept-translation">German: "Enframing" (literally: "frame," "rack," "skeleton")</p>
