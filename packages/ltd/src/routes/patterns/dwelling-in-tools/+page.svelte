<!-- Definition -->
<section class="py-16 px-6">
	<div class="max-w-3xl mx-auto">
		<h2 class="mb-8">Definition</h2>

		<div class="space-y-6 text-base opacity-70 leading-relaxed">
			<p>
				<strong>Dwelling in Tools</strong> is Heidegger's <em>Zuhandenheit</em>—the
				ready-to-hand relationship where tools disappear into use. The carpenter
				doesn't think about the hammer; attention flows through the hammer to the
				nail, the board, the house being built.
			</p>

			<p>
				This is the goal of tool design: invisibility through mastery. When a tool
				requires constant attention, it fails. When it recedes into the background,
				enabling focus on the actual work, it succeeds.
			</p>

			<p>
				Dwelling requires investment: learning the tool deeply, configuring it once
				correctly, building habits that become automatic. But once achieved, the
				reward is flow—uninterrupted work where the tool is an extension of thought.
			</p>

			<div class="p-6 border border-white/10 my-8">
				<p class="text-sm opacity-60 italic">
					"The tool recedes. The user dwells. Zuhandenheit achieved."
				</p>
			</div>
		</div>
	</div>
</section>

<!-- Principles -->
<section class="py-16 px-6 border-t border-white/10">
	<div class="max-w-3xl mx-auto">
		<h2 class="mb-8">Principles</h2>

		<div class="space-y-8">
			<div class="border border-white/10 p-6">
				<h3 class="text-lg font-semibold mb-3">Configure Once, Use Forever</h3>
				<p class="text-sm opacity-60 mb-4">
					Setup should be a one-time investment. Once configured, the tool should
					work without repeated adjustment. Configuration that requires constant
					tweaking prevents dwelling.
				</p>
				<div class="text-xs opacity-40 font-mono">
					<p class="mb-1">✓ Dotfiles that persist across machines</p>
					<p class="mb-1">✓ Sensible defaults requiring minimal overrides</p>
					<p>✓ Version-controlled configuration</p>
				</div>
			</div>

			<div class="border border-white/10 p-6">
				<h3 class="text-lg font-semibold mb-3">Workflows Become Automatic</h3>
				<p class="text-sm opacity-60 mb-4">
					Repeated actions should become muscle memory. Keyboard shortcuts,
					automated scripts, habitual patterns. Thinking about how dissolves
					into just doing.
				</p>
				<div class="text-xs opacity-40 font-mono">
					<p class="mb-1">✓ Consistent key bindings across tools</p>
					<p class="mb-1">✓ Automated repetitive tasks</p>
					<p>✓ Practiced patterns that require no thought</p>
				</div>
			</div>

			<div class="border border-white/10 p-6">
				<h3 class="text-lg font-semibold mb-3">Minimize Breakdown</h3>
				<p class="text-sm opacity-60 mb-4">
					Heidegger identifies three modes of tool failure: conspicuousness (broken),
					obtrusiveness (unsuitable), obstinacy (in the way). Design to prevent all three.
				</p>
				<div class="text-xs opacity-40 font-mono">
					<p class="mb-1">✓ Reliable operation (no random failures)</p>
					<p class="mb-1">✓ Appropriate capability (right tool for job)</p>
					<p>✓ Non-interference (doesn't block other work)</p>
				</div>
			</div>

			<div class="border border-white/10 p-6">
				<h3 class="text-lg font-semibold mb-3">Depth Over Breadth</h3>
				<p class="text-sm opacity-60 mb-4">
					Dwelling requires depth. Using many tools superficially prevents
					mastery of any. Choose fewer tools and know them completely.
				</p>
				<div class="text-xs opacity-40 font-mono">
					<p class="mb-1">✓ Master one editor, not many</p>
					<p class="mb-1">✓ Learn shortcuts incrementally until fluent</p>
					<p>✓ Invest in understanding, not just usage</p>
				</div>
			</div>
		</div>
	</div>
</section>

