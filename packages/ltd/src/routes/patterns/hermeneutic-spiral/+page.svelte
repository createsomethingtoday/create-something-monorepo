<!-- Principles -->
<section class="py-16 px-6 border-t border-white/10">
	<div class="max-w-3xl mx-auto">
		<h2 class="mb-8">Principles</h2>

		<div class="space-y-8">
			<div class="border border-white/10 p-6">
				<h3 class="text-lg font-semibold mb-3">Understanding Requires Pre-understanding</h3>
				<p class="text-sm opacity-60 mb-4">
					You can't approach anything with a blank slate. You already have assumptions,
					context, expectations. The spiral acknowledges this—work with your
					pre-understanding, let it evolve.
				</p>
				<div class="text-xs opacity-40 font-mono">
					<p class="mb-1">✓ Name your assumptions before starting</p>
					<p class="mb-1">✓ Let first iterations challenge assumptions</p>
					<p>✓ Refined understanding becomes new pre-understanding</p>
				</div>
			</div>

			<div class="border border-white/10 p-6">
				<h3 class="text-lg font-semibold mb-3">Each Pass Elevates</h3>
				<p class="text-sm opacity-60 mb-4">
					The spiral only works if each iteration produces new insight.
					Repetition without elevation is a rut, not a spiral.
					Ask: what did I learn this time that I didn't know before?
				</p>
				<div class="text-xs opacity-40 font-mono">
					<p class="mb-1">✓ Document learnings after each iteration</p>
					<p class="mb-1">✓ Incorporate insights into next pass</p>
					<p>✓ Measure deepening, not just completion</p>
				</div>
			</div>

			<div class="border border-white/10 p-6">
				<h3 class="text-lg font-semibold mb-3">Parts and Whole Evolve Together</h3>
				<p class="text-sm opacity-60 mb-4">
					As you understand parts better, the whole changes meaning.
					As the whole clarifies, the parts reveal new significance.
					Both transform together through the spiral.
				</p>
				<div class="text-xs opacity-40 font-mono">
					<p class="mb-1">✓ Revisit "understood" parts after whole clarifies</p>
					<p class="mb-1">✓ Let detail insights reshape system view</p>
					<p>✓ Neither level is ever "finished"</p>
				</div>
			</div>

			<div class="border border-white/10 p-6">
				<h3 class="text-lg font-semibold mb-3">The Spiral Has No End</h3>
				<p class="text-sm opacity-60 mb-4">
					There's no final understanding—only deeper understanding.
					The spiral continues as long as you engage. Completion is
					practical, not absolute: "deep enough for this purpose."
				</p>
				<div class="text-xs opacity-40 font-mono">
					<p class="mb-1">✓ Define "sufficient" understanding for context</p>
					<p class="mb-1">✓ Accept that return will reveal more</p>
					<p>✓ Leave paths for future deepening</p>
				</div>
			</div>
		</div>
	</div>
</section>

